package main

import (
	"github.com/stretchr/testify/assert"
	"testing"
)

func TestRunProgram(t *testing.T) {

	sampleInput1 := `.#..#
.....
#####
....#
...##`

	assert.Equal(t, 8, RunProgram(ParseInput(sampleInput1)))

	sampleInput2 := `......#.#.
#..#.#....
..#######.
.#.#.###..
.#..#.....
..#....#.#
#..#....#.
.##.#..###
##...#..#.
.#....####`

	assert.Equal(t, 33, RunProgram(ParseInput(sampleInput2)))

	sampleInput3 := `#.#...#.#.
.###....#.
.#....#...
##.#.#.#.#
....#.#.#.
.##..###.#
..#...##..
..##....##
......#...
.####.###.`

	assert.Equal(t, 35, RunProgram(ParseInput(sampleInput3)))

	sampleInput4 := `.#..#..###
####.###.#
....###.#.
..###.##.#
##.##.#.#.
....###..#
..#.#..#.#
#..#.#.###
.##...##.#
.....#.#..`

	assert.Equal(t, 41, RunProgram(ParseInput(sampleInput4)))

	sampleInput5 := `.#..##.###...#######
##.############..##.
.#.######.########.#
.###.#######.####.#.
#####.##.#.##.###.##
..#####..#.#########
####################
#.####....###.#.#.##
##.#################
#####.##.###..####..
..######..##.#######
####.##.####...##..#
.#####..#.######.###
##...#.##########...
#.##########.#######
.####.#.###.###.#.##
....##.##.###..#####
.#.#.###########.###
#.#.#.#####.####.###
###.##.####.##.#..##`

	assert.Equal(t, 210, RunProgram(ParseInput(sampleInput5)))
}
